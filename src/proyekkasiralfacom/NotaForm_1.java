public NotaForm_1(
        DefaultTableModel modelKeranjang,
        int total,
        int bayar,
        int kembalian,
        String kodeReferensi,
        int idTransaksi,
        int totalProduk
) {
    initComponents();
    setLocationRelativeTo(null);

    // TANGGAL
    lblTanggal.setText(
        new java.text.SimpleDateFormat("dd-MM-yyyy HH:mm")
                .format(new java.util.Date())
    );

    // KASIR
    lblKasir.setText(SessionUser.getNamaUser());

    // KODE & ID
    lblTanggal1.setText(kodeReferensi);
    lblTanggal2.setText(String.valueOf(idTransaksi));
    jLabel13.setText(String.valueOf(totalProduk));

    // TABLE
    DefaultTableModel modelNota = (DefaultTableModel) jTable1.getModel();
    modelNota.setRowCount(0);

    for (int i = 0; i < modelKeranjang.getRowCount(); i++) {
        modelNota.addRow(new Object[]{
            modelKeranjang.getValueAt(i, 0),
            modelKeranjang.getValueAt(i, 1),
            modelKeranjang.getValueAt(i, 2),
            modelKeranjang.getValueAt(i, 3),
            modelKeranjang.getValueAt(i, 4)
        });
    }

    lblTotal.setText("Rp " + total);
    lblBayar.setText("Rp " + bayar);
    lblKembali.setText("Rp " + kembalian);
}



